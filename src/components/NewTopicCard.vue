<script lang="ts">
import CancelButton from '@/components/shared/buttons/CancelButton.vue'
import ConfirmButton from '@/components/shared/buttons/ConfirmButton.vue'
import router from '@/router'
import { toast } from 'vue3-toastify'
import 'vue3-toastify/dist/index.css'
import { useTopicsStore } from '@/stores/topicStore'

export default {
  components: {
    CancelButton,
    ConfirmButton
  },
  data() {
    return {
      router: router,
      topicName: ''
    }
  },
  methods: {
    addNewTopic() {
      if (this.topicName == '') {
        toast.error('Digite um nome para o T처pico.')
        return
      }

      let topicsStore = useTopicsStore()
      topicsStore.addTopic(this.topicName)
      toast.success('T처pico Criado!')
      this.$emit('toggle-visibility')
    },

    closeModal() {
      this.$emit('toggle-visibility')
    }
  }
}
</script>

<template>
  <main class="modal">
    <div class="itens">
      <header>
        <h2 class="title">Novo T처pico</h2>
      </header>
      <input type="text" placeholder="Titulo" v-model="topicName" class="input-text" autofocus />
      <div class="footer">
        <ConfirmButton text="Sim" :isModalButton=true @click="addNewTopic()" />
        <CancelButton text="N찾o" :isModalButton=true @click="closeModal()" />
      </div>
    </div>
  </main>
</template>

